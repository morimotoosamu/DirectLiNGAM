#' \describe{
#'   \item{x0}{x3の影響を受ける変数}
#'   \item{x1}{x0, x2の影響を受ける変数}
#'   \item{x2}{x3の影響を受ける変数}
#'   \item{x3}{親ノードを持たない根ノード（一様分布）}
#'   \item{x4}{x0, x2の影響を受ける変数}
#'   \item{x5}{x0の影響を受ける変数}
#'   \item{x6}{x1の影響を受ける変数}
#'   \item{x7}{x4, x0の影響を受ける変数}
#'   \item{x8}{x2, x5の影響を受ける変数}
#'   \item{x9}{x3, x6の影響を受ける変数}
#' }
#'
#' @details
#' 以下の因果構造（生成式）に従って生成されています（e は一様分布のノイズ）。
#' \itemize{
#'   \item x3 = e3
#'   \item x0 = 3.0*x3 + e0
#'   \item x2 = 6.0*x3 + e2
#'   \item x1 = 3.0*x0 + 2.0*x2 + e1
#'   \item x5 = 4.0*x0 + e5
#'   \item x4 = 8.0*x0 - 1.0*x2 + e4
#'   \item x6 = 2.0*x1 + e6
#'   \item x7 = 1.5*x4 + 3.0*x0 + e7
#'   \item x8 = 0.5*x2 + 2.0*x5 + e8
#'   \item x9 = 7.0*x3 + 1.0*x6 + e9
#' }
#' 全てのノイズ項 `e` は `np.random.uniform(size=10000)` により生成されています。
#' @source \url{https://github.com/cdt15/lingam}
